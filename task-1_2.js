// Дано масив чисел. Потрібно знайти суму всіх додатних чисел у масиві, 
// які є кратними числу 3.
// Вхідні дані: масив чисел
// Вихідні дані: сума всіх додатних чисел у масиві, які є кратними числу 3.
// Приклад вихідних даних:
// let arr = [3, 5, -9, 7, 2, 8, 10, -6];

let arr = [3, 5, -9, 7, 2, 8, 10, -6]
const result = arr.filter(e => e > 0 && !(e % 3)).reduce((a, e) => a + e, 0);
console.log(result)